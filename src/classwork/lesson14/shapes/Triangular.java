package classwork.lesson14.shapes;

public class Triangular extends Shape2D {
	private final double side1;
	private final double side2;
	private final double side3;
	
	public Triangular(String name, double side1, double side2, double side3) {
		super(name);
		this.side1 = side1;
		this.side2 = side2;
		this.side3 = side3;
	}
	
	

}
